import java.util.Scanner;
public class Main
{
	public static void main(String[] args){
		Scanner in = new Scanner(System.in);
		int valor = 0;
		int valor1 = 100;
		
		//Entrada
		do {
		    System.out.println(valor);
		    valor++;
		    
		} while (valor < 101);
		
		do {
		    System.out.println(valor1);
		    valor1--;
		    
		} while (valor1 > 0);
		
		
		
	}
		
}


